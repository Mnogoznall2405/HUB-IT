PRIMARY = RGBColor(0x19, 0x76, 0xD2)
MUTED = RGBColor(0x64, 0x74, 0x8B)
WARNING = RGBColor(0xED, 0x6C, 0x02)


class ModuleSection(NamedTuple):
    title: str
    audience: str
    purpose: str
    capabilities: list[str]
    how_it_works: list[str]
    status_note: str = ""


def _style_document(doc: Document) -> None:
    normal = doc.styles["Normal"]
    normal.font.name = "Segoe UI"
    normal.font.size = Pt(11)
    for level in range(1, 4):
        style = doc.styles[f"Heading {level}"]
        style.font.name = "Segoe UI"
        style.font.color.rgb = PRIMARY
        style.font.bold = True
        if level == 1:
            style.font.size = Pt(18)
        elif level == 2:
            style.font.size = Pt(14)
        else:
            style.font.size = Pt(12)


def _add_footer(doc: Document) -> None:
    section = doc.sections[0]
    footer = section.footer
    paragraph = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = paragraph.add_run("HUB-IT | Функционал платформы | Конфиденциально")
    run.font.size = Pt(9)
    run.font.color.rgb = MUTED


def _add_paragraph(doc: Document, text: str, bold: bool = False, italic: bool = False) -> None:
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic


def _add_bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style="List Bullet")


def _add_module_section(doc: Document, module: ModuleSection, heading_level: int = 3) -> None:
    doc.add_heading(module.title, level=heading_level)
    if module.status_note:
        p = doc.add_paragraph()
        run = p.add_run(module.status_note)
        run.bold = True
        run.font.color.rgb = WARNING
    _add_paragraph(doc, f"Для кого: {module.audience}")
    _add_paragraph(doc, "Назначение", bold=True)
    _add_paragraph(doc, module.purpose)
    _add_paragraph(doc, "Что можно делать", bold=True)
    _add_bullets(doc, module.capabilities)
    _add_paragraph(doc, "Как это работает на практике", bold=True)
    _add_bullets(doc, module.how_it_works)
    doc.add_paragraph("")


